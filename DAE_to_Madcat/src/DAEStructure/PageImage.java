package DAEStructure;
public class PageImage {
	
	private int id;
	private int vdpi;
	private int hdpi;
	private int skew;
	private String path;
	private int width;
	private int height;

	public PageImage(int id, int vdpi, int hdpi, int skew, String path,
			int width, int height) {
		super();
		this.id = id;
		this.vdpi = vdpi;
		this.hdpi = hdpi;
		this.skew = skew;
		this.path = path;
		this.width = width;
		this.height = height;
	}
	
	public PageImage(){
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getVdpi() {
		return vdpi;
	}

	public void setVdpi(int vdpi) {
		this.vdpi = vdpi;
	}

	public int getHdpi() {
		return hdpi;
	}

	public void setHdpi(int hdpi) {
		this.hdpi = hdpi;
	}

	public int getSkew() {
		return skew;
	}

	public void setSkew(int skew) {
		this.skew = skew;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}
	
	
	
}
